package structs

// MinHeap — реализация бинарного мин-heap на основе слайса.
// Ключевая идея: дерево хранится в массиве без указателей,
// связи между узлами вычисляются через индексы.
//
// Инвариант мин-heap: для любого узла i выполняется:
//
//	heap[i] <= heap[left(i)]
//	и
//	heap[i] <= heap[right(i)]
//
// Следствие: минимальный элемент всегда в корне (индекс 0).
type MinHeap []int

// -----------------------------------------------------------------------------
// Формулы навигации по дереву в массиве (индексация с 0):
//
//           i
//          / \
//     2i+1   2i+2
//
// Parent(i) = (i - 1) / 2  // целочисленное деление
// Left(i)   = 2*i + 1
// Right(i)  = 2*i + 2
//
// Почему это работает:
// - Уровень 0: 1 узел (индекс 0)
// - Уровень 1: 2 узла (индексы 1,2) = 2*0+1, 2*0+2
// - Уровень 2: 4 узла (индексы 3,4,5,6) = дети от 1 и 2
// Это гарантирует «плотное» дерево (complete binary tree) без дыр.
// -----------------------------------------------------------------------------

// Len возвращает количество элементов — требуется для sort.Interface.
func (h MinHeap) Len() int {
	return len(h)
}

// Less определяет порядок: мин-heap = родитель меньше детей.
// Выносится в метод, чтобы легко сменить на макс-heap (инвертировать знак).
func (h MinHeap) Less(i, j int) bool {
	return h[i] < h[j]
}

// Swap меняет элементы местами — требуется для перестановок при sift.
func (h MinHeap) Swap(i, j int) {
	h[i], h[j] = h[j], h[i]
}

// -----------------------------------------------------------------------------
// Push: добавление элемента
// Стратегия: вставить в конец (чтобы сохранить «плотность» дерева),
// затем «всплыть» (siftUp), пока не восстановится инвариант heap.
//
// Почему в конец?
// - Иначе нарушится структура полного бинарного дерева.
// - Вставка в конец — O(1) амортизированно (append слайса).
//
// Почему siftUp, а не siftDown?
// - Новый элемент может быть меньше родителя → нужно поднимать вверх.
// - Он точно больше своих детей (их нет, он в листе) → вниз не нужно.
//
// Сложность: O(log N) — высота дерева.
// -----------------------------------------------------------------------------
func (h *MinHeap) Push(x any) {
	(*h) = append((*h), x.(int))

	h.siftUp(h.Len() - 1)
}

// -----------------------------------------------------------------------------
// Pop: извлечение минимального элемента (корня)
// Стратегия:
// 1. Запомнить корень (результат)
// 2. Переместить последний элемент на место корня (чтобы сохранить «плотность»)
// 3. Удалить последний элемент (срез слайса)
// 4. «Опустить» (siftDown) новый корень, пока не восстановится инвариант
//
// Почему последний элемент на корень?
//   - Чтобы не создавать «дыру» в дереве.
//   - Последний лист — самый «безопасный» для перемещения: у него нет детей,
//     которые могли бы нарушить инвариант снизу.
//
// Почему siftDown, а не siftUp?
// - Новый корень может быть больше детей → нужно опускать вниз.
// - У него нет родителя (он корень) → вверх не нужно.
//
// Сложность: O(log N) — высота дерева.
// -----------------------------------------------------------------------------
func (h *MinHeap) Pop() any {
	if h.Len() == 0 {
		return nil
	}

	// Запоминаем минимальный элемент (корень) для возврата
	val := (*h)[0]

	// Индекс последнего элемента (лист, который переместим на корень)
	lastIdx := h.Len() - 1

	// Перемещаем последний элемент на позицию корня
	(*h)[0] = (*h)[lastIdx]

	// Удаляем дубликат с конца (срез не меняет capacity, только len)
	*h = (*h)[:lastIdx]

	// Если heap не пуст, восстанавливаем инвариант от корня
	if len(*h) > 0 {
		h.siftDown(0)
	}

	// Строка (*h) = (*h)[:] была no-op (срез [0:len] = то же самое), удалил.
	return val
}

// siftUp поднимает элемент на позиции i вверх, пока он меньше родителя.
// Используется после Push.
func (h *MinHeap) siftUp(i int) {
	for {
		par := (i - 1) / 2 // вычисляем индекс родителя

		// Условия остановки:
		// 1. par == i → достигли корня (индекс 0: (0-1)/2 = 0)
		// 2. !Less(i, par) → элемент >= родителя, инвариант соблюдён
		if par == i || !h.Less(i, par) {
			break
		}

		// Нарушен инвариант: элемент меньше родителя → меняем местами
		h.Swap(i, par)

		// Продолжаем проверку с новой позиции (поднялись на уровень выше)
		i = par
	}
}

// siftDown опускает элемент на позиции i вниз, пока он больше одного из детей.
// Используется после Pop.
//
// Ключевой момент: выбираем для обмена НАИМЕНЬШЕГО из тройки
// (родитель, левый ребёнок, правый ребёнок), чтобы сохранить инвариант.
func (h *MinHeap) siftDown(i int) {
	for {
		min := i     // предполагаем, что родитель — наименьший
		l := 2*i + 1 // формула левого ребёнка
		r := 2*i + 2 // формула правого ребёнка

		// Проверка левого ребёнка:
		// 1. l < h.Len() → индекс валиден
		// 2. Less(l, smallest) → левый ребёнок меньше текущего кандидата
		if l < h.Len() && h.Less(l, min) {
			min = l
		}

		// Проверка правого ребёнка (аналогично)
		if r < h.Len() && h.Less(r, min) {
			min = r
		}

		// Если наименьший — не родитель, меняем местами и продолжаем опускание
		if min != i {
			h.Swap(i, min)
			i = min // переходим к позиции, куда переместили элемент
		} else {
			// Инвариант восстановлен: родитель <= обоих детей
			break
		}
	}
}
